                        <p><strong>US Equities</strong></p>
                        <p>US Indices started the week higher after U.S. Treasury Secretary Steven Mnuchin declared that the trade war between the United States and China is “on hold” following their agreement to suspend the tariff threats that shook the global markets recently. Good progress made on the weekend where the negotiators were able to set up a framework to address trade imbalances in the future. The positive sentiment favored the risk-appetite, investors demand risk-free bonds fell. Major US indices are trading near two-month highs.</p>
                        <p><strong>USD</strong></p>
                        <p>The US dollar continued higher against its peers, where the dollar index, DXY, is approaching a six-month high. The US dollar is being supported by the economic reports that confirm the strength of the US economy regardless of the slowdown in the global economy. Moreover, the easing of the U.S. – China trade war tensions will likely increase investors risk appetite, which might provide support to the US dollar against risk-off assets. FOMC May 2<sup>nd</sup> meeting minutes will be due on Wednesday.</p>
                        <p><strong>GBP </strong></p>
                        <p>The British Pound fell to fresh 2018 low ahead of key economic data this week. The Cable was facing pressures lately due to the economic slowdown in the first quarter and the strengthening US dollar. The Economic Calendar from the United Kingdom is busy where it starts with Governor Carney Speech and Inflation Report Hearings on Tuesday, Consumer Price Index on Wednesday, Retails Sales on Thursday, and GDP on Friday.</p>
                        <p><strong>Gold</strong></p>
                        <p>Gold prices drifted lower as investors favored risky assets after US-China trade war tensions eased. Gold was lately under pressure due to the rise in US Treasury yields regardless of the Geopolitical and trade war tensions. The demand for the safe-heaven is at risk as one main demand factor is fading. Gold is testing new 2018 lows.</p>
                        <p><strong>Oil</strong></p>
                        <p>Oil prices are steady near multi-year highs as the markets consume trade war news. The Brent futures traded above $80 per barrel for the first time since November 2014, however, heavy resistance was noted which took the prices back to $79 per barrel to narrow the spread between WTI and Brent. The energy services firm Baker Hughes reported that the U.S. Oil rig count was unchanged at 844 rigs.</p>
