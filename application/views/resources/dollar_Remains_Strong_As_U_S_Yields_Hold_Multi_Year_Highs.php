                        <p><strong>USD</strong></p>
                        <p>The dollar rose to a four-month high against the Japanese Yen and secured its gains against the rest major currencies. The strength in the US dollar is due to rise in Treasury yields on expectations that the Federal Reserve will raise interest rates at least two more times this year. The strong economic reports are backing up these expectations. The Initial Jobless Claims increased by 11,000 to 222,000 last week. However, the number of Americans on unemployment rolls fell to its lowest level since 1973 as it dropped by 87,000 to 1.71 million. The unemployment rate for April fell to 3.9% near a 17-year low, which confirms that the Labor market is close to full employment. Also, the Philadelphia Federal Reserve Manufacturing Index reached the highest in a year.</p>
                        <p><strong>JPY</strong></p>
                        <p>The economic reports from Japan continue to show weakness as the National CPI YoY rose less than expected. The Bank of Japan has been taking various stimulus measures to back up the economy and push inflation to its price target. Governor Koruda announced in the latest BoJ meeting, that inflation target will be attainable in the Fiscal year 2019 and they will continue with the same policy measures and go more aggressive if needed to reach that target. Oil prices could help the BoJ, as Japan is an oil importer and the appreciation in oil prices will lead to higher Import prices index and allow the inflation to pick up.</p>
