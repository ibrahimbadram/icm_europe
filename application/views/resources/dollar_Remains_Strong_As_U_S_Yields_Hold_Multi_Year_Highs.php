                        <p>The talks between the US, Canadian, and Mexican negotiators failed to deliver a proper NAFTA deal before Yesterdayâ€™s deadline. According to President Donald Trump, the quarter-century agreement is a disaster and he threatened to withdraw from it. Canadian economic reports showed that the economy is growing at a proper pace. The Consumer Price Index and Retail sales &nbsp;due today.</p>
                        <p><strong>Gold</strong></p>
                        <p>Gold is trading in a tight range near 2018 lows as the US dollar holds steady. Gold prices are down more than two percent this week, after facing pressures by the rising US treasury yields. Gold is now close to posting its largest weekly loss in five months.</p>
                        <p><strong>Oil</strong></p>
                        <p>Oil held steady near multi-year highs as markets participants expect stronger demand. Oil prices gained 70% in less than a year supported by the OPEC output plan and Geopolitical tensions. Investors are still predicting the effect of the Iranian Sanctions on oil supply. The Baker Hughes Rig Count is due today. US oil drillers added rigs for the past six consecutive weeks to reach the highest count since early 2015.</p>
