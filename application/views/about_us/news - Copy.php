          <div class="uk-margin-large-left">
            <p>London, UK –  June 8,2017: For the second year in a row, ICM, the multi-national UK based trading firm, offering forex, commodities and CFD trading announces its Official Sponsorship of the England Polo Team at Chestertons Polo in the Park 2017 happening on June 9, 10 and 11. <br>
              This year’s contribution is characterized not only by the placement of ICM’s logo on the front of all the team’s polo shirts as main sponsor, but also by a dedicated ICM marquee where guests will be able to get a complimentary caricature drawn by ICM’s professional artist Rick Coleman followed by photo print memento of the occasion and an electronic version emailed post event to further enhance on ground engagement with the crowd.<br>
              Shoaib Abedi, Director at ICM commented on the sponsorship saying: <br>
              <i>“Our participation last year left a big impact which made us keen to take part in this year’s event as well. This partnership is a reflection of our eagerness to continuously develop long-term sustainable relationships. We are delighted at ICM to be main sponsors once again”</i>.<br>
              ICM strives to be part of nationally recognized sports sponsorships as it represents a smooth progression to the firm’s global branding strategy. <br>
