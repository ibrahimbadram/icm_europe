						<p><strong>Equities</strong></p>
					<p>The global financial markets remain under pressure due to the trade conflicts between the United States and major economies. President Donald Trump tweeted on the weekend that the United States is insisting that all countries that have placed artificial trade barriers and Tariffs on US products should remove them or else the U.S. will impose more tariffs. He repeated that trade should be fair and no longer a one-way street. The Dow Jones Industrial Average came back under pressure after ending an eight-day losing streak on Friday where the future contract opened at 24600 level and dropped to trade near the low of the month at 24418.</p>
					<p><strong>USD</strong></p>
					<p>The dollar index which measures the dollar strength against a basket of major currencies fell for the second consecutive day on Friday to trade at 94.43, the lowest level since the ECB meeting on June 14th. USD/JPY fell to a two-week low of 109.37 as investors weigh the comments of president Trump. However, the greenback strengthened against major rivals during the Asian session.</p>
					<p><strong>TRY</strong></p>
					<p>The Turkish Lira gained more than one percent against the United States dollar post the Turkish Presidential elections. USD/TRY fell to trade near a two-week low at 4.5260. Despite the recent gains, the Lira is still down more than 22% this year due to the worsening economic activity and corruption.</p>
					<p><strong>Metals</strong></p>
					<p>On Friday, gold ended a five-day losing streak after finding support near a six-month low of $1261. Gold prices rose to a high of $1272 per ounce as the US dollar weakened along with US Treasury yields. However, the gains faded in earlier Asian session and gold prices are back to trade near $1265 level. The silver ounce rose to a high of $16.47 on Friday to end in green for the second day. However, the price fell today to erase Fridayâ€™s gains and trade at a low of $16.27.</p>
					<p><strong>Oil</strong></p>
					<p>West Texas Intermediate rose to a one-month high of $69.35 to gain more than 5% whereas Brent price rose more than 3% to a high of $75.80 as investors anticipated that a production increase of 1 million barrel per day is not enough to drive the price of crude down. However, oil prices retreated during the Asian session the give up some of the gains. The US Baker Hughes reported on Friday that the US oil rig count was at 862 from 863 prior. It is the first slowdown in the drilling activity since late March.</p>
