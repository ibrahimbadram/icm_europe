						<p><strong>Equities&nbsp;&nbsp;&nbsp; </strong></p>
<p>Major US indices recovered some of yesterday’s losses post the comment of the White House trade representative Peter Navaro. Despite his offensive comments towards China, Navaro is confident that the relationship between President Donald Trump and President Xi will help in resolving trade issues. On the other hand, General Electric, a member of the Dow Jones Industrial Average since early 1900, will be replaced by Walgreens, a national retail drugstore chain. “Since Walgreens is a representative of the consumer and healthcare sector in the US economy, this change will make the index a better measure of the economy", said the index compiler. The Dow Jones Industrial Average found support near a two-week low of 24569 and recovered to 24850 earlier today.</p>
<p><strong>USD</strong></p>
<p>The greenback rose against its major peers where the dollar index which measures the strength of the US dollar against a basket of six currencies rose an eleven-month high of 95.30. The dollar gained strength from the sell-off that occurred in equities. Traders will focus today on the remarks of the Federal Reserve Chairman Jerome Powell, the ECB president Mario Draghi, the Bank of Japan governor Haruhiko Koruna, and the Reserve Bank of Australia governor Philip Lowe in the ECB forum today.</p>
<p><strong>GBP</strong></p>
<p>The British pound tested a seven-month low of $1.3150 ahead of the Bank of England interest rate decision tomorrow. The Sterling was on the back foot since mid-April due to the sluggish economic reports that delayed the tightening measures from the Bank of England. After the Fed’s hawkish interest rate guidance and the ECB’s dovish interest rate guidance, how will the Bank of England act?</p>
<p><strong>Metals</strong></p>
<p>Gold prices tumbled to fresh 2018 low of $1270 on Tuesday despite the fears of a trade war occurring between China and the United States. Moreover, the price of the silver ounce fell to $16.23. The demand zone of $16.05 and $16.30 acted as key support for silver prices during the past four months.</p>
<p><strong>Oil</strong></p>
<p>Oil prices rose slightly after the American Petroleum Institute (API) reported that the US crude oil stock fell by 3.016 million barrels last week. However, oil prices will remain vulnerable to the headline ahead of the OPEC meeting on June 22ND. Oil producer will meet to discuss the oil output plan. The official crude oil inventories from the Energy Information Administration is due today.</p>
