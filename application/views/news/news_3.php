						<p><strong>USD</strong></p>
<p>The greenback rose against its major peers on Wednesday supported by the rise in the US Treasury yields. The US 10-year treasury yields ended a four-day losing streak and bounced to 2.95%, the highest level in a week whereas the dollar index, DXY, that measures the strength of the US dollar against a basket of six currencies rose to fresh 2018 highs of 95.32. The US economy continues to outperform other main economies which favors the capital flow to the dollar and US equities. In terms of economic data, the Philadelphia Fed Manufacturing Index is due today along with the Weekly Initial Jobless Claims.</p>
<p><strong>GBP</strong></p>
<p>The British pound fell to its lowest levels in seven months despite May winning the Brexit vote. The Sterling was under pressure since mid-April as the soft economic reports from the United Kingdom reduced the chances of any tightening measures by the Bank of England anytime soon. The Bank of England meeting is due today where the market widely expects that the MPC votes will remain unchanged and the bank will highlight the risks arising from trade tensions and the slowdown in the global economy.</p>
<p><strong>Metals</strong></p>
<p>Gold prices tumbled to fresh 2018 lows of $1262/ounce due to the rise in the US treasury yields and the strength of the US dollar. Despite the demand for gold as a safe asset in cases of market uncertainty, gold is down almost 3% this year which confirms the effect of the rise in the treasury yields on the non-yielding bullion. The Silver ounce also ended the fourth-consecutive day on losses and fell during the Asian session to reach a low of $16.18. Silver bullion depreciated more than 6% in a week and is approaching a major support zone near the $16.</p>
<p><strong>Oil</strong></p>
<p>Oil prices ended mixed on Wednesday as the Brent oil ended the day in red whereas the West Texas Intermediate closed on slight gains. The Spread between Brent and WTI narrowed to less than $9 yesterday after widening to more than $11 in the prior weeks. The Energy Information Administration reported yesterday that the US Crude Oil Inventories fell by 5.914 million barrels last week. However, Oil prices will remain dependent on the outcome of the OPEC meeting that is taking place tomorrow.</p>
