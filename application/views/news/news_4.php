				<h2>Dollar Retreats from 11-Month high post BOE, OPEC in Focus</h2>
                <div class="control-rte">
                     <article class="uk-article idz-article">
                        <p class="uk-article-meta"><span class="uk-label uk-label-success">Latest News </span> June 22, 2018 </p>
						 <img class="uk-margin-bottom news-list-img" src="assets/images/news/latest/news_4.jpg" alt="">
						    
						<p><strong>USD</strong></p>
						<p>The dollar index retreated from an eleven-month high of 95.53, post the announcement of Bank of England and the soft reading of the Philadelphia Fed Manufacturing index. Also, the US 10-year treasury yields tumbled to erase the gains earned on Wednesday. The Philadelphia Fed Manufacturing index fell to the lowest level since August 2017. On the other hand, the weekly initial jobless claims fell to 218K from 221k prior.</p>
						<p><strong>GBP</strong></p>
						<p>The British pound was the top performer in the market yesterday after the Bank of England Monetary Policy Vote. The BoE kept interest rates unchanged at 0.5% and the asset purchase at a target of 435 billion pounds. However, the MPC voted 6-3-0 vs an expectation 7-2-0. Andy Haldane joined Ian McCafferty and Michael Saunders in voting for a rate hike. Members expected inflation to pick up in the near term to reach the Bank of England price target. They are more confident that the slowdown in the first quarter was temporary as they have seen the household spending and sentiment bounce back strongly. The Bank of England is not considering to reduce its asset purchases before interest rates reach 1.50%. GBP/USD fell ahead of the announcement to $1.3102, its lowest level in seven months, but the Hawkish MPC vote boosted the pound to reach a high of 1.3280 ahead of the European session.</p>
						<p><strong>EUR</strong></p>
						<p>EUR/USD rebounded from an eleven-month low of $1.1508 to approach its highest level in a week ahead of the preliminary release of Manufacturing and Services PMI from Eurozone. The single currency fell almost two percent last Thursday due to the divergence in the rate path between the Federal Reserve and the European Central Bank.</p>
						<p><strong>USDCAD</strong></p>
						<p>USD/CAD rose for the sixth consecutive day to reach 1.3335, the highest level since June 23rd, 2017.&nbsp; The trade conflict between Canada and the United States is weighing on the Canadian dollar as the arising risks could delay any tightening measures by the Bank of Canada. The Canadian Consumer Price Index for May along with the Retail Sales of April will be released today.</p>
						<p><strong>Metals</strong></p>
						<p>Gold prices bounced-off a seven-month low of $1261/ounce, as the US dollar and Treasury yields tumbled. Gold prices were under pressure for the past five days. Moreover, silver ounce found support at a low of $16.18 and rose to a high of $16.35.</p>
						<p><strong>Oil</strong></p>
						<p>Today, the OPEC is holding a meeting in Vienna in order to discuss its oil output plan. The discussion is going to be tough, as some of the members are willing to raise their production level whereas Iran and Venezuela oppose the idea. Latest announcements from different oil ministers suggest that the committee will agree on an increase of 1million barrels/day. The spread between the West Texas Intermediate and the Brent oil narrowed to almost $7 yesterday. The US energy firm, Baker Hughes, will report the US oil rig count today.</p>
