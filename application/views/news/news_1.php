				<h2>Global Stocks Drop on US-China Trade War, Dollar Down</h2>
                <div class="control-rte">
                     <article class="uk-article idz-article">
                        <p class="uk-article-meta"><span class="uk-label uk-label-success">Latest News </span> June 19, 2018 </p>
						 <img class="uk-margin-bottom news-list-img" src="assets/images/news/latest/news_1.jpg" alt="">
						    
						<p><strong>Equities&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p>Major stock indices dropped earlier in the Asian session after President Donald Trump asked the US trade representative to identify $200 billion in Chinese goods for additional 10% tariffs. The trade war that is taking place between the United States and China is back to be a key mover. The negotiations that were held last month between the world’s two biggest economies failed to put an end to the trade conflict. Trump said that the new tariffs act as a response to the tariffs imposed by China on Friday. If both countries continue to retaliate, the tariffs will reach more goods and different sectors. American and Chinese consumers and companies will be hurt if this war is set to continue.</p>
<p><strong>USD</strong></p>
<p>The United States dollar fell against the Japanese Yen and Swiss Franc, as the latest development in the trade war between China and the United States weighed on investor’s risk appetite. The dollar index, DXY, which measures the strength of the US dollar against a basket of six major currencies retreated from a seven-month high in the past two days. However, the dollar gained ground against commodity currencies. The US dollar rose to its highest levels in a year against the Canadian and Australian dollar. If the threats of a trade war persist, safe haven currencies could outperform the US dollar.</p>
<p><strong>AUD</strong></p>
<p>The Australian dollar tumbled against the United States dollar to reach the lowest level since May 2017. The Reserve Bank of Australia June meeting minutes were released earlier where the RBA noted that a rise in the Australian dollar would slow inflation and growth. Moreover, the bank confirmed that the low interest rates are supporting the economy and that the current monetary policy will help inflation to pick up gradually above the 2% level.</p>
<p><strong>Gold</strong></p>
<p>Gold prices bounced-off a six-month low supported by the latest development in the trade conflict between China and the United States. Gold prices fell almost two percent on Friday after being caught in a tight trading range for more than three weeks. Gold was affected negatively by the rise in the interest rates of the Federal Reserve. However, gold might find more support if the trade war fears escalate.</p>
<p><strong>Oil</strong></p>
<p>Oil prices recovered most of Friday’s losses as investors weigh the amount of crude oil exported from the United States to China. U.S. crude exports to China fall between 350,000 and 400,000 barrels which might not affect the global supply in case of imposing tariffs. On the other hand, the Ecuador oil minister revealed that the OPEC will discuss raising the oil output in its meeting on June 22nd. Chinese tariffs on US crude and the OPEC meeting will affect oil prices throughout the week. The American Petroleum Institute (API) will report the weekly US crude oil stock today.</p>
