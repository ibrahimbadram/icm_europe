London, UK – June, 2018: ICM, the fully fledged UK licensed,  award-winning global market leader in trading Forex, commodities and CFDs, is delighted to announce its third year sponsorship of the England Polo Team at Chestertons Polo in the Park 2018 taking place on June 8, 9 and 10. <br><br>
The success achieved during the previous years continuously substantiates that the Polo sponsorship has been a gratifying one for both parties.<br><br>
This year’s contribution is characterized by the sponsorship of the International Match with England Flannels playing against ICM Ireland. The upcoming game happening on Friday, June 8 at 19:00 will gracefully display the ICM logo placed on the back of the England polo shirt and on the front of the Ireland one.<br><br>
Shoaib Abedi, Founder of ICM commented on the sponsorship:<br>
As we all know the British are credited with spreading Polo worldwide in the late 19th century and the early 20th century. This is why, we, at ICM, tend to carefully choose whom we associate our brand name with. ICM is continuously looking for global success, thus global exposure, therefore we aspire to further tying ICM’s major marketing campaigns to high-profile sporting events.<br><br>
In fact one must admit that proof of success is that we have chosen, for the third year in a row to be the Official Sponsor of the England polo team.<br><br>
 This fruitful partnership certainly strengthens the growing appeal of the ICM brand on a global level, and this is what we call the pursuit of success.<br><br>
Our vision is to become one of the dominant forces in the global financial industry and to stay on top. For me it has always been about the journey. The result will prove that hard work and perseverance pays off in the end. In my mind we are already the number one firm, we just need to let everyone else see that!!<br><br>
He further added, “ICM aims to further develop its marketing and activation initiatives in a range of international markets – for that, this sponsorship is a reflection of ICM’S brand identity. We are confident that this partnership will benefit the firm over the next few years”<br>
ICM strives to be part of nationally recognized sports sponsorships as it represents a smooth progression of the firm’s global branding strategy. <br>
         
