<p>This brief statement does not disclose all of the risks involved in trading spot foreign exchange, spot metals and other off-exchange derivative contracts. Customer should undertake such transactions only if it understands the nature of the financial markets and products offered, and the extent of the Customer’s exposure to risk. Trading in these instruments is not suitable for many members of the public. Customer should carefully consider whether trading is appropriate for Customer in light of its experience, objectives, financial resources, and relevant circumstances.</p>



<h3>Effect of "leverage" or "gearing"</h3>



<p>Transactions in spot foreign exchange, spot metals and other off-exchange derivative contracts carry a high degree of risk. The amount of initial margin is small relative to the value of the contract, so that transactions are “leveraged”.  A relatively small market movement will have a proportionately larger impact on the funds lodged by the Customer.  This may work against you as well as for the Customer. <strong>You may sustain a total loss of initial margin funds and any additional funds deposited with the firm to maintain your position.</strong> If the market moves against your position or margin levels are increased, you may be called upon to pay substantial additional funds on short notice to maintain your position. If you fail to comply with a request for additional funds within the time prescribed, your position may be liquidated at a loss and you will be liable for any resulting deficit.</p>



<h3>Risk reducing orders or strategies</h3>



<p>Placing of certain orders (e.g., ‘stop-loss’ or ‘stop-limit’ orders) that are intended to limit losses may not always be effective due to hectic market conditions or technological limitations that may make it impossible to execute such orders. Strategies using combinations of positions, such as hedging strategies, may be as risky as taking ‘long’ or ‘short’ vanilla positions.</p>



<h3>Restriction of trading and pricing relationships</h3>



<p>Illiquid Market conditions, changes in government regulation or trading restrictions with respect to certain markets may increase the risk of loss by making it difficult or impossible to effect transactions or liquidate/offset positions. ICM will bear no liability for any failure to effect any such transactions should such events arise.  Off-exchange transactions (over the counter) may be less regulated or subject to a separate regulatory regime. Before you undertake such transactions, you should familiarize yourself with applicable rules and attendant risks.</p>



<h3>Week-end risk</h3>



<p>The markets are generally closed for trading during week-ends when various news events and developments may arise causing the markets to open at a significantly different price from where they closed on Friday. Customers will not be able to use the ICM online trading software to place orders when the markets are closed. There is a substantial risk when no stop-loss orders are left to protect open positions held over weekends, especially when the markets open at levels significantly worse than their specified price.</p>



