<h3>OTC and off-exchange transactions</h3>



<p>Unlike the regulated exchanges, there are no limits on daily price movements when trading OTC instruments and other off-exchange derivatives.  Therefore not all of the Customer protections generally found in the regulated exchange markets are present in the OTC instruments and off-exchange derivatives.  OTC and off-exchange transactions may be less regulated or subject to a separate regulatory regime. Before undertaking such transactions, Customer should familiarize itself with applicable rules and risks.</p>



<h3>Counter party risk</h3>



<p>Customer enters a trade and opens a position with ICM in its capacity as a counter party. ICM online trading software is not a marketplace or an exchange.  There are no guarantees to the credit worthiness of ICM.  ICM has the right, in its sole discretion, to cease trading in any OTC and off-exchange derivative instrument at any time, and in such event Customer will be prevented from liquidating an adverse position. Such actions may result in a substantial loss to Customer.</p>



